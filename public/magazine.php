                                <h2>From the history of English porcelain</h2><br>
By the18th century, English porcelain had received international recognition. Worcester, Chelsea, Derby, Bow, Spode and some others had become well-known names for collectors and ceramists.
But unlike continental European factories which were under the patronage of princely families, British companies operated on a commercial basis and their production programme was linked to the needs and whims of the market.
Founded in 1745, the factory in Chelsea released English versions of articles in what became known as the English Rococo style. 
Chelsea also characterized original tureens in the form of animal or vegetable dishes with floral ornaments as well as elegant perfume bottles, snuff boxes for pins and bonbonnieres. 
In 1769, the business was sold and merged with the famous Derby factory.
The Bow factory opened in 1744 and was one of the biggest British ceramic enterprises of the time. 
It is also sometimes known as "New Canton" with the release of chinoiserie (blue and white porcelain with the addition of bone ash), thus imitating the Chinese products exported from the port of Canton in southern China (modern Guangzhou). 
Bow was first to master the technique of imprinting on the porcelain surface. The company existed until 1776.
The Worcester Ceramic Factory (founded in 1791) became the first to produce tea and coffee sets with the addition of so-called soapstone. 
This was in the mass production style of Meissen, Sevres porcelain and Oriental, and such products were in great demand. 
Thanks to their skilful management and responsiveness to the market, the Worcester factory was very successful.
In the early 19th century, at their plant in Stoke-on-Trent (opened in 1776), J. Spode, the son of the original owner, is said to have been responsible for the company&#39;s success in marketing English bone china. 
This product, comprising a mixture of bone ash and kaolin, produced excellent artefacts in the Regency style.
The closest competitor was the Minton factory, an company founded in 1793. It is also known for its artistic ceramics in the later Victorian style.
